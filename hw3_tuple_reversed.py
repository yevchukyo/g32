#Напишіть програму для реверсу кортежу.
#створення кортежу
tuple1 = (1, 2, 3, 4, 5)
#використовуємо зрізи для створення реверсованого кортежу на основі початкового кортежу
reversed_tuple = tuple1[::-1]
#виводимо результат
print("Початковий кортеж:", tuple1)
print("Реверсований кортеж:", reversed_tuple)
