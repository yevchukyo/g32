#Напишіть програму, щоб отримати список, відсортований у порядку зростання за останнім елементом у кожному кортежі з заданого списку непорожніх кортежів.
# Cписок кортежів
list_of_tuples = [(1, 5, 3), (2, 2, 7), (3, 1, 4), (4, 3, 6)]
#Сортуємо
sorted_list = sorted(list_of_tuples, key=lambda x: x[-1])
#виводимо результат
print("Початковий список кортежів:", list_of_tuples)
print("Відсортований список кортежів за останнім елементом:", sorted_list)
