class Calculator:
    @staticmethod
    def add(x, y):
        return x + y

    @staticmethod
    def subtract(x, y):
        return x - y

    @staticmethod
    def multiply(x, y):
        return x * y

    @staticmethod
    def divide(x, y):
        return x / y
        
    @staticmethod
    def remainder(x, y):
        return x % y
                
    @staticmethod
    def power(x, y):
        return x ** y

    @staticmethod
    def floor_divide(x, y):
        return x // y
        
                
        
